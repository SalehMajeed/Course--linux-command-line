/*
    1. printenv -> print part of environment.
    2. echo $USER -> return the user name.
        you can change user name by assign another.
    3. ps -> show process.
    4. sleep -> used for delay in terminal to take command.
    5. jobs -> tell current processing.
    6. bg 1 -> run jobs in the background.
    7. fg 1 -> run jobs in the foreground.
    8. echo $? -> return exit code of the last process.
    9. && -> used to make processes adjacent.
    10. || -> run step if one is failure.
    11. ; -> run all steps.
    12. $()-> is sub command.
     
*/